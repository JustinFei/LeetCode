/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function(fn, args, t) {
    // fn(...args)
    const timeoutId = setTimeout(() => fn.apply(null, args), t);

    const cancelFn = () => clearTimeout(timeoutId);

    return cancelFn;
};

