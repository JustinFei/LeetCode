/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = (fn, args, t) => {
  const tId = setTimeout(() => fn(...args), t);
  return () => clearTimeout(tId);
}

